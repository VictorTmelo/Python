    def getTupla(self):
        x = []

        for i in range(0, len(self.grafo)):
            for j in range(0, len(self.grafo[0])):
                if self.grafo[i][j] != 0:
                    x.append([i, j])

        for i in range(0, len(x)):
            for j in range(0, len(x[0])):
                x[i][j] = self.estados[x[i][j]]

        return x


    def generateHistogram(self):
        x = []

        for i in range(0, len(self.grafo)):
            temp = 0

            for j in range(0, len(self.grafo[0])):
                temp += self.grafo[i][j]

            x.append(temp)

        return x